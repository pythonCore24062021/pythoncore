#У другому списку зберегти індекси парних елементів першого списку. Наприклад,
#якщо дано список зі значеннями 8, 3, 15, 6, 4, 2, то другий треба заповнити значеннями 1, 4, 5, 6
#(або 0, 3, 4, 5 - якщо індексація починається з нуля),
#оскільки саме в цих позиціях першого масиву стоять парні числа.
mylist = [8, 3, 15, 6, 4, 2]
indexlist = []
for i in range(0, len(mylist)):
    if mylist[i] % 2 == 0:
        indexlist.append(i)
        print(mylist)
        print(indexlist)


